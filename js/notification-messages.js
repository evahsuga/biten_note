    /**
     * メッセージを生成する
     * @param {Object} params - パラメータ
     * @param {string} params.style - 通知スタイル (friend, coaching, festival)
     * @param {string} params.timeSlot - 時間帯 (morning, afternoon, evening)
     * @param {number} params.recordCount - 総記録数
     * @param {number} params.continueDays - 継続日数
     * @param {number} params.photoMissingCount - 写真未設定の人数
     * @param {Array} params.lastMessages - 直近3回のメッセージ
     * @returns {Object} - { type, message }
     */
    generateMessage(params) {
        const {
            style = 'friend',
            timeSlot = 'evening',
            recordCount = 0,
            continueDays = 0,
            photoMissingCount = 0,
            lastMessages = []
        } = params;

        // 1. マイルストーン該当日かチェック
        if (this.milestone[continueDays]) {
            return {
                type: 'milestone',
                message: this.milestone[continueDays]
            };
        }

        // 2. メッセージタイプを決定（確率ベース）
        const rand = Math.random();
        let messageType = 'trigger'; // デフォルト

        if (recordCount > 0 && rand < 0.2) {
            // 20%の確率で記録数メッセージ
            messageType = 'recordCount';
        } else if (photoMissingCount >= 3 && rand < 0.3) {
            // 写真未設定3人以上で30%の確率で写真メッセージ
            messageType = 'photoSuggestion';
        }

        // 3. メッセージ候補を取得
        let candidates = [];

        switch (messageType) {
            case 'recordCount':
                candidates = this.recordCount.map(m =>
                    m.replace('${count}', recordCount.toString())
                );
                break;
            case 'photoSuggestion':
                candidates = [...this.photoSuggestion];
                break;
            case 'trigger':
            default:
                candidates = [...(this.triggers[style]?.[timeSlot] || this.triggers.friend.evening)];
                break;
        }

        // 4. 直近3回を除外
        const filteredCandidates = candidates.filter(m => !lastMessages.includes(m));

        // 全て除外された場合は元の候補から選択
        const finalCandidates = filteredCandidates.length > 0 ? filteredCandidates : candidates;

        // 5. ランダム選択
        const selectedMessage = finalCandidates[Math.floor(Math.random() * finalCandidates.length)];

        return {
            type: messageType,
            message: selectedMessage
        };
    },

    /**
     * 時間帯を判定する
     * @param {number} hour - 時間（0-23）
     * @returns {string} - morning, afternoon, evening
     */
    getTimeSlot(hour) {
        if (hour >= 5 && hour < 12) {
            return 'morning';
        } else if (hour >= 12 && hour < 17) {
            return 'afternoon';
        } else {
            return 'evening';
        }
    },

    /**
     * 通知タイトルを生成する
     * @returns {string}
     */
    getNotificationTitle() {
        return '美点発見note';
    }
