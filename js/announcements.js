// ================================
// 美点発見note お知らせデータ
// ================================
// 新着判定は各ユーザーの既読ID集合（readAnnouncementIds）との「ID差分」で行う。
// 未読 = ここにある id のうち、そのユーザーの既読集合に無いもの。
//
// 【運用の鉄則】id は安定・一意・不変にすること:
//  - 既存 id を変更   → 別物=新着として全員に再通知される
//  - id を再利用       → 既読扱いになり通知されない
//  - 本文/タイトルだけの編集（id据え置き）→ 再通知しない（サイレント反映）
//
// 新しいお知らせを出すには「新しい一意の id の項目を配列先頭に足して deploy」するだけ。
// 反映はプル型（利用者がページを再読み込みしたときに反映）。
//
// 項目の形: { id, date, title, body, linkUrl?, linkLabel? }
//   linkUrl / linkLabel は任意（外部リンクを付けたいとき）。
// ================================

